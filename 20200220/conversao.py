# Convers達o para int
int_1 = int('12')
int_2 = int(12.4)
int_3 = int(12.5)
int_4 = int(12.6)

print(int_1, type(int_1))
print(int_2, type(int_2))
print(int_3, type(int_3))
print(int_4, type(int_4))

# Convers達o para float
float_1 = float(12)
float_2 = float('12')
float_3 = float('12.4')

print(float_1, type(float_1))
print(float_2, type(float_2))
print(float_3, type(float_3))

# Convers達o para str
str_1 = str(12)
