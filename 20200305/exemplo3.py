idade = int(input('Idade: '))

if (idade <= 15):
  print('Não pode')

if (idade == 16) or (idade == 17):
  print('Pode')

if (idade >= 18) and (idade <= 70):
  print('Obrigatório')

if (idade > 70):
  print('Pode')

